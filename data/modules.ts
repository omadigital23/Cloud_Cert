function applyLessonEnhancements(details: Record<string, ModuleDetails>): Record<string, ModuleDetails> {
  return Object.fromEntries(
    Object.entries(details).map(([moduleId, module]) => {
      const moduleEnhancements = lessonEnhancements[moduleId] ?? {};
      const labEnhancement = labEnhancements[moduleId];
      const appendSteps = labEnhancement?.appendSteps ?? [];

      return [
        moduleId,
        {
          ...module,
          guidedLab: {
            ...module.guidedLab,
            objective: labEnhancement?.objective ?? module.guidedLab.objective,
            steps: [...module.guidedLab.steps, ...appendSteps]
          },
          lessonSections: module.lessonSections.map((section) => ({
            ...section,
            ...(moduleEnhancements[section.id] ?? {})
          }))
        }
      ];
    })
  );
}

export const moduleDetails = applyLessonEnhancements(rawModuleDetails);
